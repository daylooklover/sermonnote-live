// Simple function for text translation
export const t = (key, lang, ...args) => {
    const selectedLang = translations[lang] ? lang : 'ko';
    const text = translations[selectedLang]?.[key];
    
    if (typeof text === 'function') {
        return text(...args);
    }
    
    return text || key;
};
